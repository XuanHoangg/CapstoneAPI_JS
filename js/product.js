function Product(name, price, screen, backCamera, frontCamera, image, description, type, id) {
    this.name = name;
    this.price = price;
    this.screen = screen;
    this.backCamera = backCamera;
    this.frontCamera = frontCamera;
    this.image = image;
    this.description = description;
    this.type = type;
    this.id = id;
}